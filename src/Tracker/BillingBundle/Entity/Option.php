<?php

namespace Tracker\BillingBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Option
 */
class Option
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $name;

    /**
     * @var string
     */
    private $price;
    
        /**
     * @var string
     */
    private $description;

    /**
     * @var \Tracker\BillingBundle\Entity\Offer
     */
    private $offer;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     * @return Option
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set price
     *
     * @param string $price
     * @return Option
     */
    public function setPrice($price)
    {
        $this->price = $price;

        return $this;
    }

    /**
     * Get price
     *
     * @return string 
     */
    public function getPrice()
    {
        return $this->price;
    }

    /**
     * Set description
     *
     * @param string $description
     * @return Option
     */
    public function setDescription($description)
    {
        $this->description = $description;

        return $this;
    }

    /**
     * Get description
     *
     * @return string 
     */
    public function getDescription()
    {
        return $this->description;
    }

    /**
     * Set offer
     *
     * @param \Tracker\BillingBundle\Entity\Offer $offer
     * @return Option
     */
    public function setOffer(\Tracker\BillingBundle\Entity\Offer $offer = null)
    {
        $this->offer = $offer;

        return $this;
    }

    /**
     * Get offer
     *
     * @return \Tracker\BillingBundle\Entity\Offer 
     */
    public function getOffer()
    {
        return $this->offer;
    }
}
